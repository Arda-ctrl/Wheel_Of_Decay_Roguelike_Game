    /// <summary>
    /// Updates the animation state based on player velocity
    /// </summary>
    /// <param name="velocity">Current movement velocity</param>
    private void UpdateAnimationState(Vector2 velocity)
    {
        if (animator == null) return;
        
                 // Determine if moving (daha hassas e≈üik deƒüeri - hafif dokunu≈ülarƒ± algƒ±la)
         bool isMoving = velocity.sqrMagnitude > 0.005f; // Daha d√º≈ü√ºk e≈üik deƒüeri
         animator.SetBool(IsMovingHash, isMoving);
        
        if (isMoving)
        {
            // Cult of the Lamb tarzƒ± hareket i√ßin animasyon kontrol√º
            float velocityMagnitude = velocity.magnitude;
            
            // Y√∂n√º belirle (hareket y√∂n√º veya bakƒ±≈ü y√∂n√º)
            float direction = 0f;
            if (Mathf.Abs(velocity.x) > 0.1f) // X y√∂n√ºnde anlamlƒ± bir hareket varsa
            {
                direction = Mathf.Sign(velocity.x);
            }
            else // Yoksa karakterin bakƒ±≈ü y√∂n√ºn√º kullan
            {
                direction = transform.localScale.x;
            }
            
            // Normalize hƒ±z: 0 = durma, 1 = maximum hƒ±z
            float normalizedSpeed = Mathf.Clamp01(velocityMagnitude / (moveSpeed * speedMultiplier));
            
                         // Hƒ±z deƒüeri i√ßin farklƒ± bir e≈üik kullanƒ±yoruz
             float runThreshold = 0.8f; // %80 ve √ºst√º hƒ±zda ko≈üma - daha geni≈ü y√ºr√ºme aralƒ±ƒüƒ±
            
                         // Hedef animasyon deƒüerini belirle, smooth ge√ßi≈ü saƒüla
             float animValue;
             if (dashCounter > 0) // Dash sƒ±rasƒ±nda
             {
                 // Ko≈üma animasyonuna hƒ±zlƒ± ge√ßi≈ü - y√∂ne g√∂re 2 veya -2
                 animValue = direction > 0 ? 2f : -2f;
             }
             else if (normalizedSpeed > runThreshold) // Normal ko≈üma
             {
                 // Ko≈üma animasyonu - aradaki deƒüerlerden ge√ßerek hedefe ula≈üacak
                 float targetValue = direction > 0 ? 2f : -2f;
                 
                 // Ko≈üma durumundaki hedef deƒüere g√∂re kademeli olarak arttƒ±r
                 if (direction > 0) // Saƒüa ko≈üma
                 {
                     // 0'dan 2'ye doƒüru kademeli ge√ßi≈ü
                     animValue = Mathf.Lerp(0f, targetValue, normalizedSpeed);
                 }
                 else // Sola ko≈üma
                 {
                     // 0'dan -2'ye doƒüru kademeli ge√ßi≈ü
                     animValue = Mathf.Lerp(0f, targetValue, normalizedSpeed);
                 }
             }
             else 
             {
                 // Y√ºr√ºme animasyonu (hedef hep 0)
                 animValue = 0f;
             }
             
             // Hareket varsa animasyon tetiklensin
             if (isMoving && normalizedSpeed > 0.05f)
             {
                 // Y√ºr√ºme animasyonunun aktifle≈ütiƒüinden emin ol
                 animator.SetBool(IsMovingHash, true);
             }
            
                         // √áok k√º√ß√ºk deƒüerleri yuvarlayƒ±p temizle (floating point hassasiyeti problemlerini √∂nle)
             if (Mathf.Abs(animValue) < 0.01f)
                 animValue = 0f;
                 
             // Smooth animation deƒüi≈üimi i√ßin lerp kullan
             float currentAnimValue = animator.GetFloat(MovementSpeedHash);
             // Daha yava≈ü ve smooth ge√ßi≈ü i√ßin lerp hƒ±zƒ±nƒ± azalttƒ±k
             float smoothAnimValue = Mathf.Lerp(currentAnimValue, animValue, Time.deltaTime * 5f); // Daha yava≈ü, smooth ge√ßi≈ü
             
             // √áok k√º√ß√ºk deƒüerleri temizle (sadece √ßok yakƒ±n olanlar)
             if (Mathf.Abs(smoothAnimValue) < 0.01f) 
             {
                 smoothAnimValue = 0f;
             }
                
            animator.SetFloat(MovementSpeedHash, smoothAnimValue);
        }
        else
        {
            // Hareket yoksa animasyon deƒüerini hƒ±zla sƒ±fƒ±ra getir
            animator.SetFloat(MovementSpeedHash, 0f);
        }
    }
