using UnityEngine;
using UnityEngine.InputSystem;
using System.Collections;
using UnityEngine.Events;

public class PlayerController : MonoBehaviour
{
    public static PlayerController Instance;
    public float moveSpeed;
    public Rigidbody2D theRB;
    public Transform gunArm;
    
    [Header("References")]
    [SerializeField] private WeaponController weaponController;
    [SerializeField] private ElementalAbilityManager elementalAbilityManager;
    [SerializeField] private Animator animator;

    private Vector2 moveInput;
    private Vector2 mousePosition;
    private Camera theCam;
    public SpriteRenderer bodySR;
    private float activeMoveSpeed;
    public float dashSpeed = 8f, dashLenght = .5f, dashCooldown = 1f, dashInvisiblity;
    [HideInInspector]
    public float dashCounter;
    private float dashCoolCounter;

    private PlayerInputActions playerInputActions;
    private bool canControl = true;
    
    // Element stack efektleri i√ßin deƒüi≈ükenler
    private float speedMultiplier = 1f;
    private float damageMultiplier = 1f;
    private float poisonDamage = 0f;
    
    // Animation parameters
    private readonly int IsMovingHash = Animator.StringToHash("isMoving");
    private readonly int MovementSpeedHash = Animator.StringToHash("movementSpeed");
    
    // Hareket dinamiƒüi i√ßin deƒüi≈ükenler
    private Vector2 currentVelocity;
    private Vector2 targetVelocity;
    private float velocitySmoothTime = 0.05f; // Daha hƒ±zlƒ± tepki i√ßin d√º≈ü√ºr√ºld√º
    private float velocityStopThreshold = 0.02f; // Bu e≈üiƒüin altƒ±ndaki hƒ±zlar sƒ±fƒ±rlanacak

    void Awake()
    {
        Instance = this;
        playerInputActions = new PlayerInputActions();

        // Subscribe to input events
        playerInputActions.Player.Movement.performed += OnMovement;
        playerInputActions.Player.Movement.canceled += OnMovement;
        playerInputActions.Player.Look.performed += OnLook;
        playerInputActions.Player.Fire.started += OnFireStart;
        playerInputActions.Player.Fire.canceled += OnFireEnd;
        playerInputActions.Player.Dash.performed += OnDash;
        
        // Test i√ßin Overflow ability'si (T tu≈üu) - Input Action kullanmadan direkt key check yapacaƒüƒ±z

        // Get WeaponController reference if not set
        if (weaponController == null)
        {
            weaponController = GetComponent<WeaponController>();
        }
    }

    private void OnEnable()
    {
        playerInputActions.Enable();
    }

    private void OnDisable()
    {
        playerInputActions.Disable();
    }
    
    private void OnDestroy()
    {
        // Unsubscribe from input events to prevent memory leaks
        if (playerInputActions != null)
        {
            playerInputActions.Player.Movement.performed -= OnMovement;
            playerInputActions.Player.Movement.canceled -= OnMovement;
            playerInputActions.Player.Look.performed -= OnLook;
            playerInputActions.Player.Fire.started -= OnFireStart;
            playerInputActions.Player.Fire.canceled -= OnFireEnd;
            playerInputActions.Player.Dash.performed -= OnDash;
            
            playerInputActions.Dispose();
        }
    }

    void Start()
    {
        theCam = Camera.main;
        activeMoveSpeed = moveSpeed;
        
        // ElementalAbilityManager'ƒ± bul
        if (elementalAbilityManager == null)
        {
            elementalAbilityManager = GetComponent<ElementalAbilityManager>();
        }
        
        // Get animator if not already assigned
        if (animator == null)
        {
            animator = GetComponent<Animator>();
            if (animator == null)
            {
                Debug.LogWarning("No Animator component found on player!");
            }
        }
        
        // OrbStackManager'ƒ± initialize et
        InitializeOrbStackManager();
        
        Debug.Log("üéÆ PlayerController initialized with Elemental Ability System");
    }
    
    /// <summary>
    /// OrbStackManager'ƒ± initialize eder
    /// </summary>
    private void InitializeOrbStackManager()
    {
        // OrbStackManager yoksa olu≈ütur
        if (OrbStackManager.Instance == null)
        {
            GameObject orbManagerGO = new GameObject("OrbStackManager");
            orbManagerGO.AddComponent<OrbStackManager>();
            Debug.Log("üîÆ OrbStackManager created and initialized");
        }
        else
        {
            Debug.Log("üîÆ OrbStackManager already exists");
        }
    }

    void Update()
    {
        if (!canControl)
        {
            // Stop all movement and actions when control is disabled
            theRB.linearVelocity = Vector2.zero;
            UpdateAnimationState(Vector2.zero);
            return;
        }

        // Cult of the Lamb tarzƒ± dinamik hareket
        // Hedef hƒ±zƒ± hesapla
        targetVelocity = moveInput * (activeMoveSpeed * speedMultiplier);
        
                 // Giri≈ü olmadƒ±ƒüƒ±nda (durma durumu) - daha hassas hareket algƒ±lama
         if (moveInput.sqrMagnitude < 0.005f) // Daha d√º≈ü√ºk e≈üik deƒüeri
         {
             // Hƒ±zƒ± daha √ßabuk azalt
             currentVelocity = Vector2.Lerp(currentVelocity, Vector2.zero, Time.deltaTime * 12f); // Biraz daha yava≈ü durdurma
            
            // Eƒüer hƒ±z √ßok d√º≈ü√ºkse tamamen durdur
            if (currentVelocity.sqrMagnitude < velocityStopThreshold)
            {
                currentVelocity = Vector2.zero;
            }
        }
        else
        {
            // Mevcut hƒ±zƒ± hedefe doƒüru yumu≈üak bir ≈üekilde deƒüi≈ütir
            Vector2 refVel = Vector2.zero; // SmoothDamp i√ßin referans
            currentVelocity = Vector2.SmoothDamp(
                currentVelocity,
                targetVelocity,
                ref refVel,
                dashCounter > 0 ? velocitySmoothTime * 0.5f : velocitySmoothTime
            );
        }
        
        // Hesaplanan hƒ±zƒ± uygula
        theRB.linearVelocity = currentVelocity;
        
        // Update animation state based on current velocity
        UpdateAnimationState(currentVelocity);

        // Handle gun arm rotation
        Vector3 screenPoint = theCam.WorldToScreenPoint(transform.localPosition);
        
        if (mousePosition.x < screenPoint.x)
        {
            transform.localScale = new Vector3(-1f, 1f, 1f);
            gunArm.localScale = new Vector3(-1f, -1f, 1f);
        }
        else
        {
            transform.localScale = Vector3.one;
            gunArm.localScale = Vector3.one;
        }

        // Calculate gun rotation
        Vector2 offset = new Vector2(mousePosition.x - screenPoint.x, mousePosition.y - screenPoint.y);
        float angle = Mathf.Atan2(offset.y, offset.x) * Mathf.Rad2Deg;
        gunArm.rotation = Quaternion.Euler(0, 0, angle);

        // Handle dash cooldown
        if (dashCounter > 0)
        {
            dashCounter -= Time.deltaTime;
            if (dashCounter <= 0)
            {
                activeMoveSpeed = moveSpeed;
                dashCoolCounter = dashCooldown;
            }
        }
        if (dashCoolCounter > 0)
        {
            dashCoolCounter -= Time.deltaTime;
        }
        
        // Test i√ßin T tu≈üu kontrol√º
        if (Input.GetKeyDown(KeyCode.T))
        {
            TestElementalOverflow();
        }
        
        // Mana test i√ßin M tu≈üu kontrol√º
        if (Input.GetKeyDown(KeyCode.M))
        {
            TestManaSystem();
        }
        
        // Orb sistem test i√ßin O tu≈üu kontrol√º
        if (Input.GetKeyDown(KeyCode.O))
        {
            TestOrbSystem();
        }
    }

    private void OnMovement(InputAction.CallbackContext context)
    {
        if (!canControl) return;
        moveInput = context.ReadValue<Vector2>();
        moveInput.Normalize();
    }

    private void OnLook(InputAction.CallbackContext context)
    {
        if (!canControl) return;
        mousePosition = context.ReadValue<Vector2>();
    }

    private void OnFireStart(InputAction.CallbackContext context)
    {
        if (!canControl) return;
        if (weaponController != null)
        {
            weaponController.StartFiring();
        }
        // Elemental yeteneklerin saldƒ±rƒ± bazlƒ± efektlerini tetikle
        if (elementalAbilityManager != null)
        {
            elementalAbilityManager.OnAttack();
        }
    }

    private void OnFireEnd(InputAction.CallbackContext context)
    {
        if (weaponController != null)
        {
            weaponController.StopFiring();
        }
    }

    private void TestElementalOverflow()
    {
        if (!canControl) return;
        
        // Test i√ßin ElementalOverflow'u tetikle
        if (elementalAbilityManager != null)
        {
            var overflowAbility = elementalAbilityManager.GetAbility(ElementType.Fire, AbilityType.ElementalOverflow) as ElementalOverflow;
            if (overflowAbility != null)
            {
                Debug.Log("üß™ Testing ElementalOverflow with T key");
                overflowAbility.UseAbility(gameObject, null, new FireElement());
            }
            else
            {
                Debug.Log("‚ùå ElementalOverflow ability not found");
            }
        }
        else
        {
            Debug.Log("‚ùå ElementalAbilityManager not found");
        }
    }
    
    private void TestManaSystem()
    {
        var manaController = PlayerManaController.Instance;
        if (manaController != null)
        {
            Debug.Log($"üíß Current Mana: {manaController.GetCurrentMana()}/{manaController.GetMaxMana()} ({manaController.GetManaPercentage() * 100:F1}%)");
            Debug.Log($"üíß PlayerManaController found and working!");
            
            // Test mana consumption
            Debug.Log("üß™ Testing 25 mana consumption...");
            bool success = manaController.ConsumeMana(25f);
            Debug.Log($"üß™ Mana consumption result: {success}");
        }
        else
        {
            Debug.LogError("‚ùå PlayerManaController not found! Please add PlayerManaController component to Player GameObject!");
        }
    }

         private void OnDash(InputAction.CallbackContext context)
     {
         if (!canControl) return;
         if (dashCoolCounter <= 0 && dashCounter <= 0)
         {
             activeMoveSpeed = dashSpeed;
             dashCounter = dashLenght;
             PlayerHealthController.Instance.MakeInvincible(dashInvisiblity);
             AudioManager.Instance.PlaySFX(8);
             
             // Dash sƒ±rasƒ±nda animasyon hedefini belirle (direkt atama yapma)
             // ƒ∞lerideki Update d√∂ng√ºs√ºnde MovementSpeed smooth bir ≈üekilde hedefe ilerleyecek
         }
     }

    public void DisableControl()
    {
        canControl = false;
        if (weaponController != null)
        {
            weaponController.StopFiring();
        }
        moveInput = Vector2.zero;
        
        if (theRB != null)
        {
            theRB.constraints = RigidbodyConstraints2D.FreezePositionX | RigidbodyConstraints2D.FreezePositionY;
        }
    }

    public void EnableControl()
    {
        if (theRB != null)
        {
            theRB.constraints = RigidbodyConstraints2D.None;
        }
        canControl = true;
    }
    
